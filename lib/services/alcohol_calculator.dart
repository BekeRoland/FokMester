  static double correctAlcohol(double measured, double temp) {
    if (measured < reads.first || measured > reads.last) {
      throw const CalculatorException('error.measuredRange');
    }
    if (temp < temps.first || temp > temps.last) {
      throw const CalculatorException('error.temperatureRange');
    }
    if (measured > 96 && temp < 9) {
      throw const CalculatorException('error.missingTableData');
    }

    final readBounds = _bounds(reads, measured);
    final tempBounds = _bounds(temps, temp);
    final lowReadValue = _interpolate(
      temps[tempBounds.$1].toDouble(),
      temps[tempBounds.$2].toDouble(),
      table[readBounds.$1][tempBounds.$1],
      table[readBounds.$1][tempBounds.$2],
      temp,
    );
    final highReadValue = _interpolate(
      temps[tempBounds.$1].toDouble(),
      temps[tempBounds.$2].toDouble(),
      table[readBounds.$2][tempBounds.$1],
      table[readBounds.$2][tempBounds.$2],
      temp,
    );
    return _interpolate(
      reads[readBounds.$1].toDouble(),
      reads[readBounds.$2].toDouble(),
      lowReadValue,
      highReadValue,
      measured,
    );
  }

  static (int, int) _bounds(List<int> values, double value) {
    for (var index = 0; index < values.length; index++) {
      if (value == values[index]) return (index, index);
      if (value < values[index]) return (index - 1, index);
    }
    return (values.length - 1, values.length - 1);
  }

  static double _interpolate(
    double x1,
    double x2,
    double y1,
    double y2,
    double x,
  ) {
    if (x1 == x2) return y1;
    return y1 + (y2 - y1) * (x - x1) / (x2 - x1);
  }

  /// Kiszámolja, mennyi vizet kell hozzáadni [volume] liter
  /// [currentAbv]%-os pálinkához, hogy [targetAbv]%-os legyen.
  /// Visszaadja a szükséges vízmennyiséget (literben) és a
  /// végtérfogatot (literben).
  static ({double waterLiters, double finalVolume}) calculateDilution({
    required double currentAbv,
    required double volumeLiters,
    required double targetAbv,
  }) {
    _validateAbv(currentAbv);
    _validateAbv(targetAbv);
    if (volumeLiters <= 0) {
      throw const CalculatorException('error.positiveVolume');
    }
    if (targetAbv >= currentAbv) {
      throw const CalculatorException('error.lowerTarget');
    }
    final double waterLiters =
        volumeLiters * (currentAbv - targetAbv) / targetAbv;
    final double finalVolume = volumeLiters + waterLiters;
    return (waterLiters: waterLiters, finalVolume: finalVolume);
  }

  static ({double spiritLiters, double waterLiters}) calculateForFinalVolume({
    required double sourceAbv,
    required double targetAbv,
    required double finalVolumeLiters,
  }) {
    _validateAbv(sourceAbv);
    _validateAbv(targetAbv);
    if (finalVolumeLiters <= 0) {
      throw const CalculatorException('error.positiveFinalVolume');
    }
    if (targetAbv >= sourceAbv) {
      throw const CalculatorException('error.lowerSourceTarget');
    }
    final spiritLiters = finalVolumeLiters * targetAbv / sourceAbv;
    return (
      spiritLiters: spiritLiters,
      waterLiters: finalVolumeLiters - spiritLiters,
    );
  }

  static ({double finalAbv, double finalVolume}) mix({
    required double firstAbv,
    required double firstVolumeLiters,
    required double secondAbv,
    required double secondVolumeLiters,
  }) {
    _validateAbv(firstAbv, allowZero: true);
    _validateAbv(secondAbv, allowZero: true);
    if (firstVolumeLiters <= 0 || secondVolumeLiters <= 0) {
      throw const CalculatorException('error.bothVolumes');
    }
    final finalVolume = firstVolumeLiters + secondVolumeLiters;
    final finalAbv =
        (firstAbv * firstVolumeLiters + secondAbv * secondVolumeLiters) /
        finalVolume;
    return (finalAbv: finalAbv, finalVolume: finalVolume);
  }

  static void _validateAbv(double value, {bool allowZero = false}) {
    if (value > 100 || value < (allowZero ? 0 : double.minPositive)) {
      throw CalculatorException(allowZero ? 'error.abvZero' : 'error.abv');
    }
  }
